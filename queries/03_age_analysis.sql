-- =====================================================
-- Запрос 3: Сравнение возраста выживших и погибших
-- =====================================================
--
-- Ключевые навыки: AVG, MIN, MAX, ROUND
-- =====================================================

SELECT 
    survived,
    ROUND(AVG(age), 2) as avg_age,
    MIN(age) as min_age,
    MAX(age) as max_age
FROM titanic
GROUP BY survived;

-- survived = 1: выжившие
-- survived = 0: погибшие
-- Обратите внимание: в данных есть пропуски (NULL) в поле age