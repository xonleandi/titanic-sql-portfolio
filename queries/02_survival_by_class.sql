-- =====================================================
-- Запрос 2: Выживаемость по классам билета
-- =====================================================
-- Показывает, сколько человек выжило/погибло в 1, 2 и 3 классе
--
-- Ключевые навыки: группировка по нескольким полям
-- =====================================================

SELECT 
    pclass,
    survived,
    COUNT(*) as count
FROM titanic
GROUP BY pclass, survived
ORDER BY pclass, survived;

-- pclass 1 = первый класс (самый дорогой)
-- pclass 3 = третий класс (самый дешевый)
-- Ожидаемый результат: в первом классе выживших больше