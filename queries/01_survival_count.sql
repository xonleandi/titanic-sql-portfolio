-- =====================================================
-- Запрос 1: Количество выживших и погибших
-- =====================================================
-- Что показывает: общее количество выживших (1) и погибших (0),
-- а также их процент от общего числа пассажиров.
-- 
-- Ключевые навыки SQL:
-- - Агрегация (COUNT)
-- - Оконные функции (SUM OVER)
-- - Группировка (GROUP BY)
-- =====================================================

SELECT 
    survived,
    COUNT(*) as count,
    ROUND(100.0 * COUNT(*) / SUM(COUNT(*)) OVER(), 2) as percentage
FROM titanic
GROUP BY survived
ORDER BY survived DESC;

-- Результат: первая строка - выжившие (1), вторая - погибшие (0)