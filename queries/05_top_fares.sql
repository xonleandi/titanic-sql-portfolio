-- =====================================================
-- Запрос 5: Топ-10 самых дорогих билетов
-- =====================================================
--
-- Ключевые навыки: сортировка, LIMIT
-- =====================================================

SELECT 
    name,
    fare,
    survived,
    pclass
FROM titanic
ORDER BY fare DESC
LIMIT 10;

-- DESC = сортировка от большего к меньшему
-- LIMIT 10 = показываем только 10 строк
-- Интересно: многие с дорогими билетами выжили