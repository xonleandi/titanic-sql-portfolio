-- =====================================================
-- Запрос 4: Выживаемость мужчин и женщин
-- =====================================================
--
-- Ключевые навыки: оконные функции с PARTITION BY
-- =====================================================

SELECT 
    sex,
    survived,
    COUNT(*) as count,
    ROUND(100.0 * COUNT(*) / SUM(COUNT(*)) OVER(PARTITION BY sex), 2) as survival_rate
FROM titanic
GROUP BY sex, survived
ORDER BY sex, survived; 

-- PARTITION BY sex означает, что процент считается отдельно
-- для мужчин и отдельно для женщин
-- Ожидаемый результат: у женщин процент выживания выше